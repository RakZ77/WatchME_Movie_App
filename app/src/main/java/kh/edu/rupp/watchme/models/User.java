package kh.edu.rupp.watchme.models;

public class User {

    private String id;
    private String email;

    // Getters
    public String getId() {
        return id;
    }

    public String getEmail() {
        return email;
    }

    // Setters
    public void setId(String id) {
        this.id = id;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
